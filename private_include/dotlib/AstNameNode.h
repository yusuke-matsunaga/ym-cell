//////////////////////////////////////////////////////////////////////
/// @class AstNameNode AstNameNode.h "AstNameNode.h"
/// @brief 名前を持つ AstNode の基底クラス
///
/// StrHandler をヘッダハンドラとして持つ．
//////////////////////////////////////////////////////////////////////
class AstNameNode :
  public AstNode
{
public:

  /// @brief コンストラクタ
  AstNameNode(const FileRegion& attr_loc, ///< [in] 属性のファイル上の位置
	      const StrHandler& header);  ///< [in] ヘッダを読み込んだハンドラ

  /// @brief デストラクタ
  ~AstNameNode();


public:
  //////////////////////////////////////////////////////////////////////
  // 外部インターフェイス
  //////////////////////////////////////////////////////////////////////

  /// @brief 名前を返す．
  const AstString*
  name() const
  {
    return mName;
  }


private:
  //////////////////////////////////////////////////////////////////////
  // 内部で用いられる関数
  //////////////////////////////////////////////////////////////////////


private:
  //////////////////////////////////////////////////////////////////////
  // データメンバ
  //////////////////////////////////////////////////////////////////////

  // 名前
  const AstString* mName;

};

